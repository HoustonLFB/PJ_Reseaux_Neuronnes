package Couche;

/**
 * Enumération TypeCouche
 * Représente les différents types de couches dans un réseau de neurones.
 */
public enum TypeCouche {
    /**
     * Représente une couche d'entrée dans un réseau de neurones.
     */
    ENTREE,

    /**
     * Représente une couche cachée dans un réseau de neurones.
     */
    CACHEE,

    /**
     * Représente une couche de sortie dans un réseau de neurones.
     */
    SORTIE
}